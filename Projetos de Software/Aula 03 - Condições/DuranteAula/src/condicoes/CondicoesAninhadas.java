package condicoes;

public class CondicoesAninhadas {

	public static void main(String[] args) {
		int nota = 85;

		if (nota >= 90) {
		    System.out.println("Excelente!");
		} else if (nota >= 70) {
		    System.out.println("Bom.");
		} else if (nota >= 50) {
		    System.out.println("Regular.");
		} else {
		    System.out.println("Insuficiente.");
		}

	}

}
